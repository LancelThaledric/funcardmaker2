    <p class="narrowp">
        Le SMF Funcard Maker 2 n'a pas pour vocation de remplacer MSE.
        MSE sera certainement toujours plus complet que cet outil, offrant plus de types de carte, cependant le SMF Funcard Maker 2 se démarque par son ergonomie et son accessibilité.
        Le but du SMF Funcard Maker 2 est de porter les bases graphiques pour Gimp et Photoshop sur une interface Web accessible à tous, afin que les funcardeurs non-initiés aux techniques graphiques ne soient plus désavantagés.
