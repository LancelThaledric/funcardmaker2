<section class="fcm-panel" id="fcm-panel-help">
    <h2>Aide</h2>
    
    <h3 class="lined">Utiliser le SMF Funcard Maker</h3>
    
    <h3 class="help-title">Prévisualisation de la carte</h3>
    <div class="help-paragraph">
        <p>
            Il vous suffit de cliquer sur la prévisualisation pour la mettre à jour.
        </p>
    </div>
    
    <h3 class="help-title">Téléchargement de la carte</h3>
    <div class="help-paragraph">
        <p>
            Une fois votre carte terminée, le gros bouton &laquo; Terminé &raquo; à gauche vous offre deux possibilités :<br/>
            Vous pouvez télécharger le fichier (format png) taille réelle, ou télécharger une version miniature de 150 Ko (format jpg) que vous pourrez uploader sur le site de la SMF. N'oubliez pas d'héberger la version HD quelque part et de proposer un lien pour la voir !
        </p>
    </div>
    
    <h3 class="help-title">Import / Export</h3>
    <div class="help-paragraph">
        <p>
            <em>Les fonctionnalités d'import/export ne sont pas encore disponibles.</em>
        </p>
        <p>
            La fonctionnalité d'import / export vous permet de sauvegarder votre travail sur le SMF Funcard Maker 2 dans un fichier *.json.<br/>
            Vous pouvez ainsi charger ce fichier et retrouver vos données pré-complétées pour continuer votre travail.
        </p>
        <p>
            Les ressources que vous uploadez sur le SMF Funcard Maker 2 sont effacées tous les jours à minuit afin de ne pas engorger les serveurs.<br/>
            Il est donc possible que vous ayez à réuploader vos ressources après un import.
        </p>
    </div>
    
    <h3 class="lined">Hébergement</h3>
    
    <h3 class="help-title">Où héberger la version HD de ma funcard ?</h3>
    <div class="help-paragraph">
        <p>
            Le SMF Funcard Maker 2 n'héberge pas votre funcard et ne la sauvegarde pas. C'est donc à vous de trouver un hébergeur pour la rendre publique.<br/>
            Voici quelques sites sur lesquels vous pouvez hébergeur votre carte :
        </p>
        <ul>
            <li><a href="http://www.noelshack.com/">NoelShack</a> est un hébergeur d'images édité par l'Odyssée Interactive, également éditeur de Jeuxvideo.com<br/>
            L'avantage de l'utiliser est que vos images ne seront pas effacées au bout d'un certain temps, contrairement à beaucoup d'autres hébergeurs.</li>
            <li><a href="http://www.deviantart.com/">DeviantArt</a> est plus qu'un hébergeur, c'est une galerie en ligne. Créez-y un compte (gratuit) et rendez vos funcards publiques sur le site de DeviantArt. N'oubliez jamais de créditer l'illustrateur et toutes les ressources utlisées dans le commentaire lorsque vous uploadez quelque chose sur DeviantArt.</li>
            <li><a href="http://www.hostingpics.net/">HostingPics</a> est un autre hébergeur d'images. Sa FAQ précise que les images hébergées le sont indéfiniment. À vérifier.</li>
        </ul>
    </div>
    
    <h3 class="help-title">Durée d'hébergement de vos ressources</h3>
    <div class="help-paragraph">
        <p>
            Le SMF Funcard Maker 2 vous permet d'utiliser vos propres ressources (fond perso, SE perso, illustration, etc.).<br/>
            Vos fichiers seront hébergés par le SMF Funcard Maker 2 le temps de la création de votre funcard.<br/>
            Comme indiqué pour chaque ressource chargée de cette manière, toutes vos ressources sont effacées tous les jours à minuit afin de ne pas engorger les serveurs.<br/>
            Si vous dépassez ce délai lors de la création de la carte, ou que vous importez votre fichier de carte après minuit, il vous faudra ré-uploader vos ressources personnelles.
        </p>
    </div>
    
    <h3 class="lined">Illustration</h3>
    
    <h3 class="help-title">Je ne connais pas l'auteur de mon illustration</h3>
    <div class="help-paragraph">
        <p>
            Il arrive qu'en naviguant sur le web on trouve une magnifique illustration mais pas son auteur.<br/>
            Il faut alors effectuer des recherches complémentaires afin de tenter de le retrouver.<br/>
            Dans tous les cas <strong>ne mettez jamais votre nom</strong> en tant qu'illustrateur, sauf si vous êtes effectivmeent l'illustrateur.<br/>
        </p>
        <p>
            <a href="https://images.google.com/">Google Images</a> propose de lancer une recherche à partir d'une image. Glissez votre fichier image dans le champ de recherche et il trouvera toutes les images semblables.<br/>
            <a href="https://www.tineye.com/">Tineye</a> est un autre moteur de recherche qui propose ce service.
        </p>
        <p>
            Sinon, l'avantage de chercher ses illustrations sur DeviantArt est que l'illustrateur figure sur la page de l'image et toute sa gallerie est accessible d'un seul clic.
        </p>
    </div>
    
    <h3 class="help-title">Recadrer l'illustration</h3>
    <div class="help-paragraph">
        <p>
            Une fois que vous avez chargé votre illustration, elle s'affiche en dessous et vous avez la possibilité de la recadrer.
            Faites glisser un des bords ou un des coins du rectangle en pointillés.<br/>
            Le bouton &laquo; Centrer &raquo; réinitialise le cadre en le centrant et couvrant un maximum l'illustration.<br/>
            Ce cadre vous garantit que votre illustration ne sera pas déformée si elle n'a pas le même rapport largeur/hauteur qu'une illustration de carte Magic.
        </p>
    </div>
    
    <h3 class="lined">Les fonds</h3>
    
    <h3 class="help-title">Je n'arrive pas à utiliser l'hybridation des fonds</h3>
    <div class="help-paragraph">
        <p>
            Une fois que vous avez cliqué sur le bouton &laquo; Hybride &raquo;,
            cliquez sur deux couleurs : la première sera à gauche et la deuxième sera à droite.
            Les deux couleurs sélectionnées seront marquées d'un G et d'un D signifiant respectivement Gauche et Droite.
        </p>
    </div>
    
    <h3 class="help-title">Est-ce possible de faire un fond hybride à trois couleurs ?</h3>
    <div class="help-paragraph">
        <p>
            L'hybridation à plus de deux couleurs n'existe pas sur les cartes officielles. Ce n'est pas prévu dans le SMF Funcard Maker 2, pour le moment.
        </p>
    </div>
    
    <h3 class="help-title">Comment créer un fond perso ?</h3>
    <div class="help-paragraph">
        <p>
            La création de fond personnels n'est pas encore disponible dans le SMF Funcard Maker 2 (C'est prévu, mais il y a plus urgent).<br/>
            Utilisez votre logiciel de traitement d'image préféré (Photoshop, Gimp, Paint <i>(hum...)</i> ) et utilisez des bases disponibles <a href="http://www.smfcorp.net/forum/post-1700-1-tout-pour-le-funcardeur.html" title="Tout pour le Funcardeur ! - SMFCorp">ici, sur le forum de SMFCorp</a>.
        </p>
        <p>
            Si vous souhaitez créer un fond perso à partir de fonds officiels, la meilleure technique consiste à utiliser le SMF Funcard Maker 2 afin de générer une carte entièrement vide avec le fond que vous voulez, puis de télécharger le rendu. Modifiez-le comme vous voulez, puis utilisez-le comme fond perso dans votre SMF Funcard Maker favori.
        </p>
    </div>
    
    <h3 class="lined">Texte</h3>
    
    <h3 class="help-title">Le titre de la carte est trop long</h3>
    <div class="help-paragraph">
        <p>
            Le dépassement de titre et de type ne sont pas encore gérés par le SMF Funcard Maker 2, mais cela viendra, patience !
        </p>
    </div>
    
    <h3 class="help-title">Insérer un caractère spécial</h3>
    <div class="help-paragraph">
        <p>
            Le SMF Funcard Maker vous permet d'insérer facilement les caractères spéciaux les plus utilisés dans les funcards grâce aux boutons en-dessous de champs de texte.
        </p>
        <p>
            Si vous voulez insérer un autre caractère spécial, il vous faudra le copier manuellement dans le champ en question. Cependant il se peut que ce caractère ne soit pas présent dans la police de caractères, faites donc attention.
        </p>
    </div>
    
    <h3 class="lined">Symboles d'édition</h3>
    
    <h3 class="help-title">Le Symbole d'Édition que ne souhaite utiliser n'est pas dans la liste</h3>
    <div class="help-paragraph">
        <p>
            Les symboles d'édition les plus récents ne sont pas encore disponibles. Vous pouvez cependant tenter de le trouver quelque part sur le web et l'utiliser comme SE perso.
        </p>
    </div>
    
    <h3 class="help-title">Comment créer un Symbole d'Édition personnalisé ?</h3>
    <div class="help-paragraph">
        <p>
            Il vous faudra vous munir d'un logiciel d'édition d'image comme Gimp ou Photoshop.<br/>
            Une fois la forme de votre symbole tracée, il vous suffira de lui appliquer un contour et un dégradé afin de le faire ressembler à un véritable Symbole d'Édition.<br/>
            Les couleurs utilisées pour les dégradés sont données dans les bases graphiques pour Gimp et Photoshop.
        </p>
    </div>
    
    <h3 class="lined">Bugs, suggestions, etc.</h3>
    
    <h3 class="help-title">Au secours ! un BUG !</h3>
    <div class="help-paragraph">
        <p>
            Comment ça ? Vous avez trouvé un BUG ?! Fichtre... ಠ╭╮ಠ<br/>
            Un logiciel sans bugs ça n'existe pas ! Merci de bien vouloir vous rendre sur le <a href="https://bitbucket.org/Thaledric/funcardmaker2/issues?status=new&status=open" title="Aller sur le Bug Tracker" target="_blank">Bug Tracker</a> (grâce à ce lien ou à celui dans le menu tout en haut), puis cliquez sur "Create Issue" à droite. N'oubliez pas de m'indiquer comment reproduire le bug !
        </p>
        <p>
            <i>(Pensez aussi à regarder si quelqu'un n'a pas déjà déposé un ticket pour ce bug...)</i>
        </p>
    </div>
    
    <h3 class="help-title">Suggestions de fonctionnalités</h3>
    <div class="help-paragraph">
        <p>
            Vous aimez tellement le SMF Funcard Maker 2, mais vous trouvez qu'il lui manque toutefois quelque chose...
            Vous pouvez vous rendre sur le <a href="https://bitbucket.org/Thaledric/funcardmaker2/issues?status=new&status=open" title="Aller sur le Bug Tracker" target="_blank">Bug Tracker</a> (grâce à ce lien ou à celui dans le menu tout en haut), puis cliquez sur "Create Issue" à droite. N'oubliez pas d'indiquer le type d'issue (&laquo; enhancement &raquo; pour une suggestion, car tout est en anglais).
        </p>
    </div>
    
